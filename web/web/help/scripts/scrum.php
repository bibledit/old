<h2><?php echo Locale_Translate::_("Daily Scrum Meeting") ?></h2>
<p>
 <?php echo Locale_Translate::_("On each day of a sprint, the Bible translation team holds daily meetings.") ?>
 <?php echo Locale_Translate::_("These meetings are called: Daily scrum meetings.") ?>
 <?php echo Locale_Translate::_("The scrum meetings are held in the same place and at the same time.") ?>
 <?php echo Locale_Translate::_("The meetings are typically held in front of Task Board.") ?>
 <?php echo Locale_Translate::_("The daily scrum meeting is best held in the morning, because it assists in setting the context for the work of that day.") ?>
 <?php echo Locale_Translate::_("During the meeting, all members stand, as this keeps the discussion short and relevant.") ?>
 <?php echo Locale_Translate::_("The scrum meeting is strictly time-boxed to 15 minutes.") ?>
</p>
<br>
<p>
 <?php echo Locale_Translate::_("There is a difference between people who are 'committed' to the Bible translation project, and those who are 'involved' in it.") ?>
 <?php echo Locale_Translate::_("Scrum affords special status to those who are committed.") ?>
 <?php echo Locale_Translate::_("Many teams enforce a rule in which only those who are committed are allowed to talk during the daily scrum meeting.") ?>
</p>
<br>
<p>
 <?php echo Locale_Translate::_("All team members are required to attend the scrum meetings.") ?>
 <?php echo Locale_Translate::_("The Scrum Master and the and Product Owner (usually the Bible Translation Manager) are committed team members.") ?>
 <?php echo Locale_Translate::_("They are expected to attend and participate.") ?>
 <?php echo Locale_Translate::_("Anyone else, for example a Church leader, a Committee member, or a Translator from another team, is allowed to attend but is there to listen only.") ?>
