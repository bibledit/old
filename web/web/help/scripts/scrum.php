<br>
<p><?php echo Locale_Translate::_("During the daily Scrum, each team member answers the following three questions:") ?></p>
<p>1. <?php echo Locale_Translate::_("What did you do yesterday?") ?></p>
<p>2. <?php echo Locale_Translate::_("What are you going to do today?") ?></p>
<p>3. <?php echo Locale_Translate::_("Are there any impediments in your way?") ?></p>
<p><?php echo Locale_Translate::_("The Scrum Master asks each team member to answer these questions, the member speaks, the Scrum Master thanks the member, and then goes to the next member.") ?></p>
<br>
<p>
 <?php echo Locale_Translate::_("By focusing on what each person did yesterday and will do today, the team gains an understanding of what work has been done and what work remains.") ?>
 <?php echo Locale_Translate::_("The daily scrum meeting is not a status update meeting in which the boss is finding out about who is slow in his work.") ?>
 <?php echo Locale_Translate::_("Rather, it is a meeting in which team members make commitments to each other.") ?>
 <?php echo Locale_Translate::_("If a translator says: Today, I will prepare chapter 10 and 11 of Matthew, everyone knows that in tomorrow's meeting, he will say whether or not he did finish.") ?>
 <?php echo Locale_Translate::_("This has the wonderful effect of helping a team realize the significance of these commitments, and that their commitments are to each other, and not to anyone else.") ?>
</p>
<br>
<p>
 <?php echo Locale_Translate::_("Any impediments that are raised in the daily scrums become the Scrum Master's responsibility to resolve as quickly as possible.") ?>
 <?php echo Locale_Translate::_("Typical impediments are:") ?>
