<p>3. <?php echo Locale_Translate::_("I'm struggling to understand ... and would like to discuss it with someone.") ?></p>
<p>4. <?php echo Locale_Translate::_("The ... has asked me to work on something else for a few days.") ?></p>
<p>
 <?php echo Locale_Translate::_("In cases where the Scrum Master cannot remove these impediments directly himself, he still takes responsibility for making sure someone on the team does quickly resolve the issue.") ?>
