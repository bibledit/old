<br>
<p>
 <?php echo Locale_Translate::_("The scrum meeting is not the place for solving problems or resolving issues.") ?>
 <?php echo Locale_Translate::_("Issues that are raised are usually dealt with by one or two team members immediately after the scrum.") ?>
